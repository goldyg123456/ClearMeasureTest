namespace NumberAndNameGenerator
{
    /// <summary>
    /// This enum represents the different types of lines that can be printed in the output.
    /// </summary>
    internal enum LineType
    {
        /// <summary>
        /// The line contains only the first name.
        /// </summary>
        FirstName,
        /// <summary>
        /// The line contains only the last name.
        /// </summary>
        LastName,
        /// <summary>
        /// The line contains both the first and last name.
        /// </summary>
        FullName
    }
}
